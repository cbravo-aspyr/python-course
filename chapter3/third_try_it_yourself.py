#Store 5 places to visit
places_to_visit = ["fiji", "hawaii", "pr", "Rusia", "Africa"]
#Print OG list
print(places_to_visit)
#use sorted to print list in order
print(sorted(places_to_visit))
#print again to show is still in order
print(places_to_visit)
#Reversed order
places_to_visit.reverse()
#print
print(places_to_visit)
#Reverse again and print as it was Originally
places_to_visit.reverse()
print(places_to_visit)
#user sort to change to alphabetical order
places_to_visit.sort()
print(places_to_visit)
#user sort(reverese=True) to change to reverse alphabetical order
places_to_visit.sort(reverse=True)
print(places_to_visit)
